function substractTwoNumbers(num1, num2) {
     var total = num1 - num2;
     return total;
}

var firstnumber = 25;
var secondnumber = 22;
var result = substractTwoNumbers(firstnumber, secondnumber);
console.log('Substracted result:', result)


function divideTwoNumbers(num1, num2) {
     var total = num1 / num2;
     return total;
}

var firstnumber = 25;
var secondnumber = 22;
var result = divideTwoNumbers(firstnumber, secondnumber);
console.log('divided:', result)